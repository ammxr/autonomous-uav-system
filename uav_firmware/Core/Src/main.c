void Set_Ailerons_From_Roll(uint16_t roll)
{
  int deflection = (int)roll - 1500;

  uint16_t left_servo  = clamp_pwm(1500 + deflection);
  uint16_t right_servo = clamp_pwm(1500 + deflection);

  __HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_1, left_servo);
  __HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_2, right_servo);
}

void Set_Ruddervators(uint16_t pitch, uint16_t yaw) {
	int deflection_pitch = (int)pitch - 1500;
	int deflection_yaw = (int)yaw - 1500;

	uint16_t left_servo = clamp_pwm(1500 + deflection_pitch + deflection_yaw);
	uint16_t right_servo = clamp_pwm(1500 + deflection_pitch - deflection_yaw);

	__HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_3, left_servo);
	__HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_4, right_servo);
}

uint16_t clamp_pwm(int value)
{
  if (value < 1000) return 1000;
  if (value > 2000) return 2000;
  return (uint16_t)value;
}

void Parse_Controller_Message(char *msg)
{
  int t, r, p, y;
  // Expected format: T1500R1500P1500Y1500\n
  if (sscanf(msg, "T%dR%dP%dY%d", &t, &r, &p, &y) == 4) // the == 4 to ensure all 4 values are received.
  {
    throttle_pwm = clamp_pwm(t);
    roll_pwm     = clamp_pwm(r);
    pitch_pwm    = clamp_pwm(p);
    yaw_pwm      = clamp_pwm(y);

    Set_Ailerons_From_Roll(roll_pwm);
    Set_Ruddervators(pitch_pwm, yaw_pwm);
  }
}

void Process_Controller_UART(void)
{
  // Non-blocking check for 1 byte at a time
  if (HAL_UART_Receive(&huart2, &rx_byte, 1, 0) == HAL_OK)
  {
    if (rx_byte == '\n')
    {
      rx_buffer[rx_index] = '\0';
      Parse_Controller_Message(rx_buffer);
      rx_index = 0;
      memset(rx_buffer, 0, sizeof(rx_buffer));  // After the buffer (aka the complete message string) is passed to the controller, reset buffer to 0s
    }
    else if (rx_byte != '\r') // ignore carraige return since a message can be like HELLO\r\n we do not care about \r since \n already indicates the next message.
    {
      if (rx_index < sizeof(rx_buffer) - 1)
      {
        rx_buffer[rx_index++] = rx_byte;
      }
      else
      {
        rx_index = 0;
        memset(rx_buffer, 0, sizeof(rx_buffer)); // if somehow the entire buffer (64 bytes/chars) is used without seeing the \n indicating the message ended. We wipe everything in the buffer and move on.
      }
    }
  }
}
